  <h2>MU, CoxHealth and Mercy Break New Ground to Address Physician Shortage</h2>
                  <p id="date">Posted September 11, 2015</p>
                   <p>The University of Missouri, <a href="http://www.coxhealth.com/" target="_blank">CoxHealth</a> and <a href="https://www.mercy.net/practice/mercy-hospital-springfield" target="_blank">Mercy</a> health systems of Springfield are celebrating their partnership 
            designed to address a critical shortage of physicians in the state and nation by breaking ground for the 
            Patient-Centered Care Learning Center in Columbia. A groundbreaking ceremony held July 9, 2015, celebrated the 
            start of construction for a $42.5 million medical education building at the University of Missouri and the new 
            MU clinical campus in Springfield, Missouri.
         </p>
         <p>The goal of the project is to train more physicians for Missouri by expanding the class size of the MU School of 
            Medicine and adding an MU clinical campus in Springfield.
         </p>
         <p>The Patient-Centered Care Learning Center in Columbia is scheduled to open in 2017. MU medical students will 
            begin training at the clinical campus in Springfield in 2016.
         </p>
         <p>“This project will provide more than 300 additional physicians for Missouri, add more than $390 million annually 
            to the state’s economy and create 3,500 new jobs,” said University of Missouri System President Tim Wolfe.
         </p>
         <p>“Our state and nation are in desperate need of more physicians to serve a growing elderly population, 
            increasing numbers of patients living with chronic illnesses, and many others gaining access to health 
            care through insurance reform,” said  R. Bowen Loftin, PhD, chancellor of the University of Missouri. 
         </p>
         <p>“This expansion of the MU School of Medicine will allow us to attract great students from Missouri to 
            attend medical school and then stay in Missouri and work in Missouri as physicians," said Patrick Delafontaine, 
            MD, Hugh E. and Sarah D. Stephenson Dean of the MU School of Medicine.
         </p>
         <p>“The vision for the building is to create a learning space that reflects our goal of educating physicians to 
            provide effective patient-centered care for the people of Missouri and beyond,” said Linda Headrick, MD, senior 
            associate dean at MU School of Medicine. “That is why the building is called the ‘Patient-Centered Care Learning 
            Center.’ This new learning environment will explicitly focus the students’ attention on the people they are preparing 
            to serve.”
         <p>More than 90 percent of Missouri’s counties lack adequate access to health care professionals. At the same time, Missouri ranks among the top 20 states with citizens over age 65, who require more medical care. The number of elderly people with multiple chronic diseases likely will double by 2030.</p>
         <p>“As we see firsthand with our family medicine residency program, medical students tend to practice where they grew up 
            or where they attended school,” said Steve Edwards, president and CEO of CoxHealth. “By giving students more options 
            for clinical training in other hospitals and physician practices, we are educating them on the diverse health needs of 
            our state and increasing the odds of putting more physicians in Springfield and southwest Missouri.”
         </p>
          <p>“Everyone will still spend the first two years at MU, and this expanded space is a well thought out environment that is comfortable, functional and strongly supports student-driven learning,” said Molly Johnson, MU second-year medical student. “This partnership will help the MU School of Medicine continue to draw high-achieving students because of cutting-edge educational experiences with lots of opportunities and technological advances.”</p>
         <p>The groundbreaking marks the fulfillment of a vision shared by MU, CoxHealth and Mercy that began eight years ago 
            when the three organizations began working together to train more physicians to work in Missouri and beyond. 
         </p>
         
            <blockquote>“This partnership will help the MU School of Medicine continue to draw high-achieving students because of cutting-edge educational experiences with lots of opportunities and technological advances."<cite>Molly Johnson, MU medical student</cite></blockquote>
         
   <p>“Partnering with MU, CoxHealth and Mercy for this clinical campus is another example of like-minded organizations 
      working together to do what’s right for patients in the future,” said David Barbe, MD, vice president of regional 
      operations for Mercy Springfield communities. “Our doctors are excited about getting to practice medicine while also 
      teaching and mentoring our next generation of providers.” 
   </p>
   <p>“Thanks to the statewide support we have received, we now have the partners, resources and talent we need to 
      transform medical education in Missouri,” Loftin said. “We hope MU’s unique partnership will serve as a model for 
      other areas of the state that face shortages of health care professionals.”
   </p>

              
